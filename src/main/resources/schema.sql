create table course
(
    id         bigint generated by default as identity,
    title      varchar(255) not null,
    creator_id bigint       not null,
    created_at timestamp    not null,
    updated_at timestamp,
    primary key (id)
);

create table ns_user
(
    id         bigint generated by default as identity,
    user_id    varchar(20) not null,
    password   varchar(20) not null,
    name       varchar(20) not null,
    email      varchar(50),
    created_at timestamp   not null,
    updated_at timestamp,
    primary key (id)
);

create table question
(
    id         bigint generated by default as identity,
    created_at timestamp    not null,
    updated_at timestamp,
    contents   clob,
    deleted    boolean      not null,
    title      varchar(100) not null,
    writer_id  bigint,
    primary key (id)
);

create table answer
(
    id          bigint generated by default as identity,
    created_at  timestamp not null,
    updated_at  timestamp,
    contents    clob,
    deleted     boolean   not null,
    question_id bigint,
    writer_id   bigint,
    primary key (id)
);

create table delete_history
(
    id            bigint not null,
    content_id    bigint,
    content_type  varchar(255),
    created_date  timestamp,
    deleted_by_id bigint,
    primary key (id)
);

create table session
(
    id                         bigint generated by default as identity,
    amount                     bigint      not null,
    number_of_maximum_members  bigint      not null,
    session_type               varchar(20) not null,
    session_status             varchar(20) not null,
    session_recruitment_status varchar(20) not null,
    image_id                   bigint,
    start_at                   timestamp,
    end_at                     timestamp
);

create table session_member
(
    ns_user_id bigint not null,
    session_id bigint not null,
    constraint session_member_pk primary key (ns_user_id, session_id)
);

create table image
(
    id         bigint generated by default as identity,
    width      bigint      not null,
    height     bigint      not null,
    image_type varchar(10) not null,
    size       bigint      not null
)